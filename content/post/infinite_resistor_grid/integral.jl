# ╔═╡ 9c8701e8-cd9d-4b8c-b682-10b425ba64fa
grid_resitance_integrand(n, m) = function ((x, y))
	(1 - cos(n * x + m * y)) / (2 - cos(x) - cos(y))
end

# ╔═╡ d2040d60-6e56-4d35-866e-e7d084b9eb8f
T = Float64

# ╔═╡ 31fa7e61-2973-4cc4-b14b-b81738536693
function grid_resitance(n, m, T = T)
	n == m == 0 && return zero(T)
	res1 = hcubature(grid_resitance_integrand(n, m), T.((0, 0)), (2T(pi), 2T(pi)))[1]
	res1 = res1 / pi / pi / 4
	return res1
end

# ╔═╡ 8638dd28-62e0-471a-988a-0c4add963529
gridmax = 35

# ╔═╡ 40a2595f-e12a-4761-954d-31bb6733bce1
grid_pnts = collect(Iterators.product(0:gridmax, 0:gridmax))

# ╔═╡ 1ba665b7-9c78-479c-a5f4-199b9469610b
grid_resists = let
	resists = fetch.(map(grid_pnts) do (n, m)
		n <= m && return Threads.@spawn grid_resitance(n, m)
		0
	end)
	Symmetric(resists)
end

